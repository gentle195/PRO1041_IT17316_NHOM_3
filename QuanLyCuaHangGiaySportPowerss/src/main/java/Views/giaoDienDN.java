    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String manv = txtMa.getText();
        String matKhau = new String(txtMK.getText());
        try {
            List<NhanVien> ls = service.getNV(manv);
            NhanVien nv = ls.get(0);
            System.out.println(nv);
            if (nv != null) {
                String matKhau2 = nv.getMatKhau();
                if (matKhau.equals(matKhau2)&&manv.equalsIgnoreCase("ql")) {
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                    QuanLyView ql = new QuanLyView();
                    this.dispose();
                    ql.setVisible(true);
                }else if(matKhau.equals(matKhau2)&&!manv.equalsIgnoreCase("ql")){
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                    NhanVienView nvv = new NhanVienView();
                    this.dispose();
                    nvv.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "Sai mật khẩu!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Sai tên đăng nhập!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

