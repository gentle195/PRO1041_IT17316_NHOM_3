            chiTietHoaDonViewModel.setTenSP((String) tbldssanpham.getValueAt(row, 1));
            chiTietHoaDonViewModel.setSoLuong(Integer.valueOf(sl));
            chiTietHoaDonViewModel.setDonGia((BigDecimal) tbldssanpham.getValueAt(row, 2));
