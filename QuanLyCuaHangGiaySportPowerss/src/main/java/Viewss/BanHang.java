                    for (int i = 0; i < listhdct.size(); i++) {
                        slsp = slsp + (listhdct.get(i).getSoLuong());
                    }
                    chiTietHoaDonViewModel.setSoLuong(slsp);
                    //                HDCT.updateSL(hd,id);
