 private void loaddatatable() {
        DefaultTableModel modeltable = new DefaultTableModel();
        List<HoaDonViewModel> ns = hoaDonService.getall();
        modeltable = (DefaultTableModel) tbHoaDon.getModel();
        modeltable.setRowCount(0);
        for (HoaDonViewModel n : ns) {
            modeltable.addRow(new Object[]{
                n.getIdHD(), n.getMa(), n.getNgayTao(), n.getNgayThanhToan(), n.getNgayShip(),
                n.getNgayNhan(), n.getTenNgNhan(), n.getDiaChi(), n.getSdt(),
                n.getTinhTrang() == 1 ? "Hoàn thành" : "Đã huỷ"});
        }
    }

    private HoaDon tonghop() {
        HoaDon aN = new HoaDon();
        aN.setMa(txtMa.getText());
        aN.setNgayTao(txtNgayTao.getText());
        aN.setNgayThanhToan(txtNgayTT.getText());
        aN.setNgayShip(txtNgShip.getText());
        aN.setNgayNhan(txtNgayNhan.getText());
        aN.setTenNgNhan(txtTenNN.getText());
        aN.setDiaChi(txtDiaChi.getText());
        aN.setSdt(txtSdt.getText());
        if (rdHT.isSelected()) {
            aN.setTinhTrang(1);
        } else if (rdDH.isSelected()) {
            aN.setTinhTrang(2);
        }
        return aN;
    }

